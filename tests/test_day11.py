from ..day11 import parse_data, part_one, count_flashes, \
    neighbours, flash, part_two


def test_parse_data():
    data = parse_data("tests/data_test_day_11.txt")
    assert type(data) == list
    assert len(data) == 10
    assert data[0] == [5, 4, 8, 3, 1, 4, 3, 2, 2, 3]
    assert data[9][9] == 6


def test_count_flashes():
    assert count_flashes(
        [
            [4, 4, 8, 4, 1, 4, 4, 0, 0, 0],
            [2, 0, 4, 4, 1, 4, 4, 0, 0, 0],
            [2, 2, 5, 3, 3, 3, 3, 4, 9, 3],
            [1, 1, 5, 2, 3, 3, 3, 2, 7, 4],
            [1, 1, 8, 7, 3, 0, 3, 2, 8, 5],
            [1, 1, 6, 4, 6, 3, 3, 2, 3, 3],
            [1, 1, 5, 3, 4, 7, 2, 2, 3, 1],
            [6, 6, 4, 3, 3, 5, 2, 2, 3, 3],
            [2, 6, 4, 3, 3, 5, 8, 3, 2, 2],
            [2, 2, 4, 3, 3, 4, 1, 3, 2, 2],
        ]
    ) == 8


def test_neighbours():
    assert neighbours(10, 10, 0, 0) == [(0, 1), (1, 0), (1, 1)]
    assert neighbours(10, 10, 9, 9) == [(8, 8), (8, 9), (9, 8)]


def test_flash():
    assert flash(
        [
            [4, 4, 8, 4, 1, 4, 4, 0, 0, 0],
            [2, 0, 4, 4, 1, 4, 4, 0, 0, 0],
            [2, 2, 5, 3, 3, 3, 3, 4, 9, 3],
            [1, 1, 5, 2, 3, 3, 3, 2, 7, 4],
            [1, 1, 8, 7, 3, 0, 3, 2, 8, 5],
            [1, 1, 6, 4, 6, 3, 3, 2, 3, 3],
            [1, 1, 5, 3, 4, 7, 2, 2, 3, 1],
            [6, 6, 4, 3, 3, 5, 2, 2, 3, 3],
            [2, 6, 4, 3, 3, 5, 8, 3, 2, 2],
            [2, 2, 4, 3, 3, 4, 1, 3, 2, 2],
        ]
    ) == [
            [5, 5, 9, 5, 2, 5, 5, 1, 1, 1],
            [3, 1, 5, 5, 2, 5, 5, 2, 2, 2],
            [3, 3, 6, 4, 4, 4, 4, 6, 0, 5],
            [2, 2, 6, 3, 4, 4, 4, 4, 9, 6],
            [2, 2, 9, 8, 4, 1, 4, 3, 9, 6],
            [2, 2, 7, 5, 7, 4, 4, 3, 4, 4],
            [2, 2, 6, 4, 5, 8, 3, 3, 4, 2],
            [7, 7, 5, 4, 4, 6, 3, 3, 4, 4],
            [3, 7, 5, 4, 4, 6, 9, 4, 3, 3],
            [3, 3, 5, 4, 4, 5, 2, 4, 3, 3],
    ]


